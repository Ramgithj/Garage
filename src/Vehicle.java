
public class Vehicle {
//Attributes
    
    protected int bhp;
    protected int year;
    protected String colour;
    protected String make;
    protected String model;
    
 //Constructors   
    public Vehicle(int bhp,int year, String colour, String make, String model) {
        this.bhp = bhp;
        this.year = year;
        this.colour = colour;
        this.make = make;
        this.model = model;
    }

}
